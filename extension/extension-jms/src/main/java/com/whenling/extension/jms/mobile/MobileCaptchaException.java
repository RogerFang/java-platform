package com.whenling.extension.jms.mobile;

public class MobileCaptchaException extends Exception {

	private static final long serialVersionUID = -3945232569536168414L;

	public MobileCaptchaException() {
		super();
	}

	public MobileCaptchaException(String message) {
		super(message);
	}

	public MobileCaptchaException(Throwable cause) {
		super(cause);
	}

	public MobileCaptchaException(String message, Throwable cause) {
		super(message, cause);
	}

}
